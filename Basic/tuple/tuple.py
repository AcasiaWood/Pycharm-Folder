# create tuples

numbers = (1, 2, 3)  # tuple values cannot be erased.
print(numbers)

# tuple indexing

alphabets = ('a', 'b', 'c')
print(alphabets[0])

# tuple slicing

devices = ('desktop', 'laptop')
print(devices[0:])

# add tuple

prime_numbers = (2, 3, 5)
synthetic_numbers = (4, 6, 8)
print(prime_numbers + synthetic_numbers)

# multiply tuple

languages = ['korean', 'japanese', 'russian']
print(languages * 5)

# find tuple length

bag = ['wallet', 'cellphone', 'keycard']
print(len(bag))
