def addition(a, b):
    c = a + b
    return c
def subtraction(a, b):
    c = a - b
    return c
def multiply(a, b):
    c = a * b
    return c
def division(a, b):
    c = a / b
    return c
a = int(input())
b = int(input())
print(addition(a, b))
print(subtraction(a, b))
print(multiply(a, b))
print(division(a, b))
